export function getAllPickerEmojis(): string[] {
  const seen = new Set<string>();
  const out: string[] = [];
  for (const cat of EMOJI_PICKER_CATEGORIES) {
    for (const e of cat.emojis) {
      if (!seen.has(e)) {
        seen.add(e);
        out.push(e);
      }
    }
  }
  return out;
}

export function filterCategoriesBySearch(query: string): EmojiCategory[] {
  const q = query.trim().toLowerCase();
  if (!q) return EMOJI_PICKER_CATEGORIES;
  return EMOJI_PICKER_CATEGORIES.filter(
    (c) =>
      c.labelFr.toLowerCase().includes(q) ||
      c.keywords.some((k) => k.includes(q)),
  );
}
